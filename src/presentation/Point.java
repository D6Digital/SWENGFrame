package presentation;

/**
 * 
 * @author sp807
 *
 */
public class Point {
	
	int num, xCoord, yCoord;

	/**
	 * @return the individual number corresponding to that point
	 */
	public int getNum() {
		return num;
	}

	/**
	 * @param num the number of that point
	 */
	public void setNum(int num) {
		this.num = num;
	}

	/**
	 * @return the x coordinate of the point
	 */
	public int getX() {
		return xCoord;
	}

	/**
	 * @param x set the x coordinate of the point
	 */
	public void setX(int x) {
		this.xCoord = x;
	}

	/**
	 * @return the y coordinate of the point
	 */
	public int getY() {
		return yCoord;
	}

	/**
	 * @param y set the y coordinate of the point
	 */
	public void setY(int y) {
		this.yCoord = y;
	}

}
