package presentation;

public class Point {
	
	int num, xCoord, yCoord;

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public int getX() {
		return xCoord;
	}

	public void setX(int x) {
		this.xCoord = x;
	}

	public int getY() {
		return yCoord;
	}

	public void setY(int y) {
		this.yCoord = y;
	}

}
